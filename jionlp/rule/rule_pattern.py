COMMON_TIME_PATTERN_2 = '((晚|早)(上|间)|(深|昨|前|今)?夜(里|晚|间)?|(今|傍|昨|当)晚|(早|凌|今|清)晨|午后|日前)'

TIME_PATTERN = '(' + '|'.join(
    [COMMON_TIME_PATTERN_1, COMMON_TIME_PATTERN_2, WEEK_PATTERN,
     YMD_HMS_PATTERN, SEASON_PATTERN,
     YMD_PATTERN_1, YMD_PATTERN_2, YMD_PATTERN_3, YMD_PATTERN_4,
     HMS_PATTERN_1, HMS_PATTERN_2, HMS_PATTERN_3]) + ')'

